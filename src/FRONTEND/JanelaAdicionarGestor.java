    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (Lusername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o username", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lusername.requestFocus();
            return;
        }
        if (Lnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o nome do gestor", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lnome.requestFocus();
            return;
        }
        if (Lemail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o email", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lemail.requestFocus();
            return;
        }
        if (Lpass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a password", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lpass.requestFocus();
            return;
        }

        String username = Lusername.getText();
        String nome = Lnome.getText();
        String email = Lemail.getText();
        String password = Lpass.getText();

        try {
            sistema.getUtilizadores().adicionarUtilizadorUsername(new Gestor(username, password, email, nome));
            dispose();
            JanelaListarGestores j = new JanelaListarGestores(sistema, bd);
            j.setVisible(true);

        } catch (UtilizadorDuplicadoException ex) {
            JOptionPane.showMessageDialog(this, "Username j√° utilizado!");
            Lusername.requestFocus();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

