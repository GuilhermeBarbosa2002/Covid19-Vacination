    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (Lnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a sua localidade", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lnome.requestFocus();
            return;
        }
        if (Lemail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza a sua morada", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lemail.requestFocus();
            return;
        }
        if (Lpassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o n√∫mero de postos", "Dados em falta", JOptionPane.WARNING_MESSAGE);
            Lpassword.requestFocus();
            return;
        }

        String nome = Lnome.getText();
        String email = Lemail.getText();
        String password = Lpassword.getText();

        Gestor g = (Gestor) sistema.getUtilizadores().getGestor(username);
        g.setNome(nome);
        g.setEmail(email);
        g.setPassword(password);

        JanelaListarGestores j = new JanelaListarGestores(sistema, bd);
        j.setVisible(true);
        dispose();

    }//GEN-LAST:event_jButton3ActionPerformed

